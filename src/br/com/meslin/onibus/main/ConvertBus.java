	private void doAll() {
		String buffer;
		/** Map<horario, Map<ordem, posicao>>dados */
		Map<String, Map<String, Posicao>>dados = new LinkedHashMap<String, Map<String, Posicao>>();
		
		for(String arquivo: arquivos) {
			String horario = arquivo.substring(9,15); 
			dados.put(horario, new LinkedHashMap<String, Posicao>());
			
			System.err.println("Processando arquivo " + arquivo + " at " + new Date());
			buffer = StaticLibrary.readFile(directory + "/" + arquivo);
			JSONObject jsonObject = new JSONObject(buffer);
			JSONArray jsonData = jsonObject.getJSONArray("DATA");
			
			
			for(int i=0; i<jsonData.length(); i++) {
				JSONArray jsonBus = jsonData.getJSONArray(i);
				String ordem = jsonBus.getString(ORDEM);
				double lat = jsonBus.getDouble(LATITUDE);
				double lon = jsonBus.getDouble(LONGITUDE);
				
				Posicao posicao = new Posicao(lat, lon);
				dados.get(horario).put(ordem, posicao);
			}
		}
/*		for(Map.Entry<String, Map<String, Posicao>> horario: dados.entrySet()) {
			for(Map.Entry<String, Posicao> dado: horario.getValue().entrySet()) {
				System.out.print(dado.getValue().getLat() + ", ");
			}
			System.out.println();
		}
*/		
		// cria uma lista de ordens de ônibus sem repetição
		Set<String>ordens = new TreeSet<String>();
		for(Map.Entry<String, Map<String, Posicao>> horario: dados.entrySet()) {
			for(Map.Entry<String, Posicao> dado: horario.getValue().entrySet()) {
				ordens.add(dado.getKey());
			}	
		}
		
		System.out.print("\n");
		for(Map.Entry<String, Map<String, Posicao>> horario: dados.entrySet()) {
			System.out.print("," + horario.getKey());
		}
		for(String ordem: ordens) {
			System.out.print("\n" + ordem);
			for(Map.Entry<String, Map<String, Posicao>> horario: dados.entrySet()) {
				System.out.print(",");
				if(horario.getValue().get(ordem) != null) {
					System.out.print(horario.getValue().get(ordem).getLat());
				}
			}
		}
		System.out.print("\n");
		for(Map.Entry<String, Map<String, Posicao>> horario: dados.entrySet()) {
			System.out.print("," + horario.getKey());
		}
		for(String ordem: ordens) {
			System.out.print("\n" + ordem);
			for(Map.Entry<String, Map<String, Posicao>> horario: dados.entrySet()) {
				System.out.print(",");
				if(horario.getValue().get(ordem) != null) {
					System.out.print(horario.getValue().get(ordem).getLon());
				}
			}
		}
	}

